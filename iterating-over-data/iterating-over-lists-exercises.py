# Example: Dining Out
# Follow these steps for each example:
options = ["the place I'm craving but is too far away",
           "the place we always go to",
           "that place that just opened but looks too fancy"
           ]
for option in options:
    print(f"What about getting food from {option} tonight?")

# Read through the code and identify:
# What is the list? The list is called options

# What is each element? Each element represents the dining options a user can choose from. They include: "the place I'm craving but is too far away", "the place we always go to", and "that place that just opened but looks too fancy". All of these list elements are strings.

# What do we name each element? We name each element option

# How do we use each element in the loop? By iterating through each element and storing each element as a value in the option variable inside the print statement.

