# Answer: Merge sort divides an array into two sub-arrays repeatedly into smaller and smaller sub-arrays until the sub-arrays have only one element in it/a size of one. When the sub-arrays have been divided, the sub-arrays will be sorted and merged together into one array. It has a log linear time complexity and the factors that contribute to this time complexity are the division step, merge step, base case, and counting of levels.
